export function getSubject(subjectIdOrSlug: string): Subject | undefined {
  return curriculum.find(
    (subject) => subject.id === subjectIdOrSlug || subject.name.toLowerCase() === subjectIdOrSlug.toLowerCase(),
  );
}

export function getChapter(chapterIdOrSlug: string): Chapter | undefined {
  return allChapters.find(
    (chapter) => chapter.id === chapterIdOrSlug || chapter.slug === chapterIdOrSlug,
  );
}

export function getTopic(topicIdOrSlug: string): Topic | undefined {
  return allTopics.find((topic) => topic.id === topicIdOrSlug || topic.slug === topicIdOrSlug);
}

export function getTopicPath(topicIdOrSlug: string): string | undefined {
  for (const subject of curriculum) {
    for (const chapter of subject.chapters) {
      const topic = chapter.topics.find(
        (candidate) => candidate.id === topicIdOrSlug || candidate.slug === topicIdOrSlug,
      );
      if (topic) return `/learn/${subject.id}/${chapter.slug}/${topic.slug}`;
    }
  }
  return undefined;
}

export function getLesson(slug: string): Lesson | undefined {
  return lessons.find((lesson) => lesson.slug === slug || lesson.id === slug);
}

export function getSimulation(slug: string): Simulation | undefined {
  return simulations.find((simulation) => simulation.slug === slug || simulation.id === slug);
}

export function getQuestionsForTopic(topicId: string): PracticeQuestion[] {
  return practiceQuestions.filter((question) => question.topicId === topicId);
}

export function getPlannerItemsForDate(date: string): PlannerItem[] {
  return plannerItems.filter((item) => item.date === date);
}

export function getSubjectName(subjectId: SubjectId): string {
  return curriculum.find((subject) => subject.id === subjectId)?.name ?? subjectId;
}
