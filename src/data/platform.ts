export const subjectProgress = curriculum.map((subject) => {
  const topics = subject.chapters.flatMap((chapter) => chapter.topics);
  const completed = topics.filter((topic) => topic.status === "completed").length;
  const progress = Math.round(
    topics.reduce((total, topic) => total + topic.progress, 0) / Math.max(topics.length, 1),
  );

  return {
    subjectId: subject.id,
    completed,
    total: topics.length,
    progress,
  };
});

